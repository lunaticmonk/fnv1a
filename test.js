import test from 'ava';
import m from '.';

test('32-bit', t => {
	t.is(m('', 32), 2166136261);
	t.is(m('h', 32), 3977000791);
	t.is(m('he', 32), 1547363254);
	t.is(m('hel', 32), 179613742);
	t.is(m('hell', 32), 477198310);
	t.is(m('hello', 32), 1335831723);
});

